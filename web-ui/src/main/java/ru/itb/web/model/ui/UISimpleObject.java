package ru.itb.web.model.ui;

public class UISimpleObject {
    private Integer id;
    private String name;

    public UISimpleObject(){}

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
